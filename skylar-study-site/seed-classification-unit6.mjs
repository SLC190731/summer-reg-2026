async function seed() {
  const conn = await pool.getConnection();
  try {
    // 先刪除原有 unit_id=6 的分類題（避免重複）
    const [delRes] = await conn.query('DELETE FROM classification_questions WHERE unit_id = 6');
    console.log(`已刪除單元六舊資料：${delRes.affectedRows} 筆`);

    for (const q of questions) {
      const sql = `INSERT INTO classification_questions
        (unit_id, question, categories, items, correctAnswer, subject, grade, createdAt, updatedAt)
        VALUES (?, ?, ?, ?, ?, ?, ?, NOW(), NOW())`;
      const values = [
        q.unitId,
        q.question,
        JSON.stringify(q.categories),
        JSON.stringify(q.items),
        JSON.stringify(q.correctAnswer),
        'people',
        4,
      ];
      await conn.execute(sql, values);
      console.log(`✓ 匯入：${q.question.substring(0, 30)}...`);
    }

    const [rows] = await conn.query('SELECT COUNT(*) AS c FROM classification_questions WHERE unit_id = 6');
    console.log(`\n✅ 單元六現有分類題總數：${rows[0].c}`);
  } finally {
    conn.release();
    await pool.end();
  }
}

seed().catch((e) => {
  console.error('❌ 匯入失敗:', e);
  process.exit(1);
});
